    months = {
        1: 'ianuarie',
        2: 'februarie',
        3: 'martie',
        4: 'aprilie',
        5: 'mai',
        6: 'iunie',
        7: 'iulie',
        8: 'august',
        9: 'septembrie',
        10: 'octombrie',
        11: 'noiembrie',
        12: 'decembrie'
    }

    return months.get(month_id)